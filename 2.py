# == significa igual a quanto
print(7 + 7)  #Adição
print(7 - 7)  #Subtração
print(7 * 7)  #Multiplicação
print(7 / 7)  #Divisão
print(7 ** 7) #Potência
print(7 // 7) #Divisão inteira
print(7 % 7)  #Resto da divisão
print(7 ** (1/2)) #Raiz quadrada
print(7 ** (1/3)) #Raiz cubica

print("abc"*7 ) #Repitir oq está entre aspas

print("Olá,{:20}" .format()) #Em 20 espaços
print("Olá,{:>20}" .format()) #Em 20 caracteres a direita
print("Olá,{:<20}" .format()) #Em 20 caracteres a esquerda
print("Olá,{:^20}" .format()) #Em 20 caracteres centralizado
print("Olá,{:=>20}" .format()) #Em 20 caracteres subistituindo os espaços por =
print("Olá,{:.3f}" .format())  #Definir o tanto de casas decimais eu quero que apareça

